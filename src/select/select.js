var pk = pk || {};
/**
Create a new select control. Controls can be converted from existing `select` or `ul` elements, or generated by passing an `options` parameter

HTML

	<!-- Generate from an existing element -->
	
    <select id='selectFromEl'>
        <option value='val1'>
            option 1
        </option>
        <option value='val2'>
            option 2
        </option>
        <option value='val3'>
            option 3
        </option>
        <option value='val4'>
            option 4
        </option>
        <option value='val5'>
            option 5
        </option>
    </select>
	
	<!-- Generate from passing an options object -->
	
	<div id='selectFromObject'></div>

Javascript
	
	// Single select with no dropdown and pre-selected value
	
	oui.select({
		element: document.getElementById('selectFromEl'),
		value: 'val3'
	});	
	
	// Multiple select with dropdown
	
	oui.select({
		element: document.getElementById('selectFromObject'),
		options:[
			{
				value:'val1',
				name:'value 1'
			},
			{
				value:'val2',
				name:'value 2'
			},
			{
				value:'val3',
				name:'value 3'
			},
			{
				value:'val4',
				name:'value 4'
			},
			{
				value:'val5',
				name:'value 5'
			},
		]
	});	
		
	
@class oui.select
@constructor
@param options {Object}
@param options.element {Object} DOM element to convert to control
@param options.options {Array} Object array of control options
@param options.options.value {String} Option value
@param options.options.name {String} Option name (displayed label)
@param options.options.selected {Boolean} Option selected state
@param [options.value=0] {String} Value of initially selected option, defaults to the attribute value set on the passed element, or `0`
@param [options.name=oui-select-RandInt] {String} Name of underlying input control, defaults to the attribute value set on the passed element, or `oui-select-RandInt`
@param [options.placeholder=string] {String} Placeholder when no options selected, defaults to the attribute value set on the passed element, or `Please select...`
@param [options.multiple=false] {Boolean} Whether to allow multiple options to be selected, defaults to the attribute value set on the passed element, or `false`
@param [options.dropdown=false] {Boolean} Display options in dropdown list
@param [options.tabindex=0] {Number} Tabindex of control, defaults to the attribute value set on the passed element, or `0`
@param [options.disabled=false] {Boolean} Disabled state of control, defaults to the attribute value set on the passed element, or `false`
@return Object {Object} Consisting of original DOM element (item `0`) and class methods (see below)
@chainable
*/
(function(pk) {
    oui.select = function(opt) {

        var el = opt.element,
            options = opt.options || [],
            inputValue = opt.value || oui.attribute(el, 'value') || [],
            inputName = opt.name || oui.attribute(el, 'name') || 'oui-select-' + oui.getRand(1, 999),
            dropdown = Boolean(opt.dropdown),
            inputMultiple = (Boolean(opt.multiple) === true || oui.attribute(el, 'multiple')) ? true : false,
            inputPlaceholder = opt.placeholder || oui.attribute(el, 'placeholder') || 'Please select...',
            // listeners=opt.listeners === undefined ? {} : opt.listeners,            
            inputDisabled = (opt.disabled || el.getAttribute('disabled')) ? 'disabled' : '',
            inputTabIndex = opt.tabindex || el.getAttribute('tabindex') || 0;

        inputName = (inputMultiple && inputName.indexOf('[]') === -1) ? inputName + '[]' : inputName.replace('[]', '');

        // input value is now an array
        inputValue = oui.toArr(inputValue);

        // populate options if none present and the underlying element is SELECT or UL
        if (options.length === 0 && (el.nodeName === "SELECT" || el.nodeName === "UL")) {
            for (var i = 0; i < el.children.length; i++) {
                var oVal = oui.attribute(el.children[i], 'value'),
                    oName = el.children[i].innerHTML;
                oVal = oVal || oName;
                if (oui.attribute(el.children[i], 'selected') && inputValue.indexOf(oVal) === -1) {
                    inputValue.push(oVal);
                }
                options.push({
                    name: oName,
                    value: oVal
                });
            }
        }

        /*jshint multistr:true */
        var tpl = "<div class='oui-select " + (inputMultiple ? 'oui-select-multiple' : '') + " " + (dropdown ? 'oui-select-dropdown' : '') + " " + (inputDisabled ? 'oui-disabled' : '') + "' tabindex='" + inputTabIndex + "'>\
                <input type='hidden' name='" + inputName + "'/>\
                <div class='oui-select-value " + (!inputValue || inputValue.length < 1 ? 'oui-placeholder' : '') + "'>sdsd</div>\
            <ul>";
        for (var o = 0; o < options.length; o++) {
            tpl += "<li class='oui-option' data-value='" + options[o].value + "'>" + options[o].name + "</li>";
        }
        tpl += "</ul></div>";

        el.innerHTML = '';
        el = oui.replaceEl(el, tpl);

        var triggerEl = el.children[1],
            inputEl = el.children[0],
            optionsEl = el.children[2];

        if (dropdown && !inputDisabled) {
            var overlayEl = document.body.insertBefore(oui.createEl("<div class='oui-overlay'></div>"), document.body.children[0]);
            oui.bindEvent('click', overlayEl, function() {
                oui.removeClass(overlayEl, 'oui-show');
                oui.toggleClass(el, 'oui-show');
            });
            oui.bindEvent('click', el, function(e) {

                if (oui.attribute(e.target, 'data-value') && oui.hasClass(e.target, 'oui-select-value-tag')) {
                    inputValue = oui.collide(inputValue, oui.attribute(e.target, 'data-value'), 2);
                    updateValue();
                }


                if (oui.hasClass(e.target, 'oui-select-value-tag') || (inputMultiple && oui.hasClass(e.target, 'oui-option')) || !oui.hasClass(el, 'oui-show')) {
                    oui.addClass(overlayEl, 'oui-show');
                    oui.addClass(el, 'oui-show');
                } else {
                    oui.removeClass(overlayEl, 'oui-show');
                    oui.removeClass(el, 'oui-show');
                }

            });
        }


        function updateValue() {
            var valueHTML = '';
            if (!inputMultiple) {
                inputValue.splice(1, inputValue.length - 1);
            }
            for (var o = 0; o < options.length; o++) {
                options[o].selected = (inputValue.indexOf(options[o].value) !== -1) ? true : false;
                oui.toggleClass(optionsEl.children[o], 'oui-selected', options[o].selected);
                if (options[o].selected) {
                    valueHTML = inputMultiple ? valueHTML += "<span class='oui-select-value-tag' data-value='" + options[o].value + "'>" + options[o].name + "</span>" : options[o].name;
                }
            }
            triggerEl.innerHTML = valueHTML ? valueHTML : inputPlaceholder;
            if (inputValue.length < 1) {
                oui.addClass(triggerEl, 'oui-placeholder');
            } else {
                oui.removeClass(triggerEl, 'oui-placeholder');
            }
            // update underlying input element
            inputEl.value = inputMultiple ? inputValue : inputValue.join('');
        }
        if (!inputDisabled) {
            oui.bindEvent('click', optionsEl, function(e) {
                if (e.target.nodeName === "LI") {
                    inputValue = inputMultiple ? oui.collide(inputValue, options[oui.getIndex(e.target)].value, 3) : oui.collide(inputValue, options[oui.getIndex(e.target)].value);
                    updateValue();
                }
            });
        }
        updateValue();
        return {
            0: el
        };
    };
    return pk;
})(pk);
